		<h1>Assignment 3 - Fade in</h1>
	</header>

	<header>
		<h1>Embracing accessibility as a team effort</h1>
	</header>

	<div class="fade-in">
		<p>
			As developers, we often find ourselves at the forefront of ensuring our products are
			accessible to all users. However, accessibility should not be the sole responsibility of
			developers. In this blog post, we will explore the importance of making accessibility a
			collaborative effort across various teams and why it’s essential to integrate accessibility
			into your process from the start.
		</p>
	</div>

	<div class="fade-in">
		<h2 id="hey-developer">
			Hey, developer!<a
				href="#hey-developer"
				aria-label="Permalink to &quot;Hey, developer!&quot;"
				class="post-single__heading-target"
			></a>
		</h2>
		<p>
			We just want you to know: you simply can’t do accessibility on your own. And you shouldn’t,
			and that’s okay. As a developer with an accessibility role, it’s often the case that you have
			the (implied) responsibility to make sure accessibility is accounted for. Of course, you can
			make sure your implementation is correct and follows WCAG best practices. But if the concept,
			design or content strategy do not take accessibility into account, there is only so much you
			can do: If the design of a component you received isn’t accessible, it will never lead to an
			accessible implementation. You can’t polish a turd.
		</p>
	</div>

	<div class="fade-in">
		<p>
			It’s great if you, as a developer, are the driving force behind accessibility. Every
			initiative has to start somewhere, but at times, the task can seem overwhelmingly large. This
			is why it’s crucial to have the right support in place. A lack of support can cause some
			serious problems: fields that include helping others and advocacy are the highest likely to
			burn out. This is especially true when you feel your influence is limited or when you have to
			fight for a seat at the table. And on top of having to prove accessibility is important to the
			organization, you also must prove it to your coworkers.
		</p>
		<note
			><p>
				Learn more on this topic by checking out the presentation <a
					href="https://www.deque.com/axe-con/wp-content/uploads/2022/12/The-Accessibility-to-Burnout-Pipeline_a11y.pdf"
					>The Accessibility To Burnout Pipeline (PDF, external link)</a
				> by Shell Little.
			</p></note
		>
	</div>

	<div class="fade-in">
		<p>
			So, it’s important to make accessibility a team effort. And moving accessibility up or left in
			the project cycle ensures that accessibility will be integrated and prioritized.
		</p>
	</div>
	<div class="fade-in">
		<h2 id="what-does-shift-left-mean-for-accessibility">
			What does shift left mean for accessibility?<a
				href="#what-does-shift-left-mean-for-accessibility"
				aria-label="Permalink to &quot;What does shift left mean for accessibility?&quot;"
				class="post-single__heading-target"
			></a>
		</h2>
		<p>
			“Shifting left” focuses on starting with accessibility in mind from the very beginning of a
			project. Instead of waiting until the work is done and then focusing on accessibility, the
			goal is to integrate best practices and compliance in the early stages of design and
			development. This is why all teams should work together. It is also proven to be more <a
				href="https://www.w3.org/WAI/business-case/">cost-effective (external link)</a
			> and it can help help promote an inclusive mindset and a culture of prioritizing accessibility
			across all teams.
		</p>
	</div>

	<div class="fade-in">
		<h2 id="how-to-shift-left-and-integrate-accessibility-across-teams">
			How to shift left and integrate accessibility across teams<a
				href="#how-to-shift-left-and-integrate-accessibility-across-teams"
				aria-label="Permalink to &quot;How to shift left and integrate accessibility across teams&quot;"
				class="post-single__heading-target"
			></a>
		</h2>
	</div>

	<div class="fade-in">
		<p>To make accessibility a priority, it’s essential to involve multiple teams:</p>
		<ul>
			<li>
				Designers play a pivotal role. By designing with accessibility in mind, they can create user
				interfaces that are inclusive.
			</li>
			<li>
				Project managers should prioritize accessibility as a feature and set clear goals for its
				implementation. They can also help allocate resources and track progress.
			</li>
			<li>Developers should incorporate accessibility testing into their processes.</li>
			<li>
				Content creators, including writers and marketers, should be educated on creating accessible
				content, such as using descriptive alt text for images and writing clear, concise text.
			</li>
			<li>
				Obtaining the support of leadership is crucial for creating a culture of accessibility.
				Executives should champion accessibility initiatives.
			</li>
		</ul>
	</div>

	<div class="fade-in">
		<h2 id="shifting-left-at-grrr">
			Shifting left at GRRR<a
				href="#shifting-left-at-grrr"
				aria-label="Permalink to &quot;Shifting left at GRRR&quot;"
				class="post-single__heading-target"
			></a>
		</h2>
		<p>
			Honestly, our process isn’t perfect yet. Rest assured, we are fully committed to making it
			work. Throughout our journey, we’ve implemented fundamental changes that have significantly
			contributed to our progress. While I’ll highlight a few, please note that there are others as
			well:
		</p>
	</div>

	<div class="fade-in">
		<h3 id="design-system">
			Design System<a
				href="#design-system"
				aria-label="Permalink to &quot;Design System&quot;"
				class="post-single__heading-target"
			></a>
		</h3>
		<p>
			Incorporating accessibility in our component library or design system made our projects more
			inclusive. We’ve taken steps to ensure that every component is tested and adjusted
			accordingly. This not only streamlines the development process but also sets a solid
			foundation for accessible components.
		</p>
	</div>

	<div class="fade-in">
		<h3 id="design-reviews">
			Design Reviews<a
				href="#design-reviews"
				aria-label="Permalink to &quot;Design Reviews&quot;"
				class="post-single__heading-target"
			></a>
		</h3>
		<p>
			Designer and developer collaborate in regular Design Reviews during projects. This proactive
			approach serves as a safeguard against accessibility issues, ensuring that only accessible
			work is presented to the client. These are often valuable and educational moments where
			different roles can learn a lot from each other.
		</p>
	</div>

	<div class="fade-in">
		<h3 id="quick-scans-during-sprints">
			Quick Scans during sprints<a
				href="#quick-scans-during-sprints"
				aria-label="Permalink to &quot;Quick Scans during sprints&quot;"
				class="post-single__heading-target"
			></a>
		</h3>
		<p>
			Instead of waiting until the end of all sprints, integrating so called WCAG quick scans into
			the ongoing development process ensures that accessibility issues are identified and addressed
			right away, minimizing the risk of cumulative errors. This proactive approach not only aligns
			with Agile principles of continuous improvement but also contributes to the creation of more
			inclusive work from the outset.
		</p>
	</div>

	<div class="fade-in">
		<blockquote>
			"Accessibility is not a check afterwards. We have made accessibility audits a part of our
			development sprints" <cite>Jelmer Boomsma, Founder</cite>
		</blockquote>
	</div>

	<div class="fade-in">
		<h2 id="shifting-left-can-be-hard">
			Shifting left can be hard<a
				href="#shifting-left-can-be-hard"
				aria-label="Permalink to &quot;Shifting left can be hard&quot;"
				class="post-single__heading-target"
			></a>
		</h2>
		<p>
			Getting this process rolling can be challenging, especially when you’re not getting the
			high-fives you expected. Here are some tips to kickstart things:
		</p>
	</div>

	<ul>
		<li class="fade-in">
			<p>
				<strong>Link it to your company values</strong>: Show how accessibility aligns with your
				organization’s bigger goals and values. Being more inclusive and boosting your company’s
				rep? Who can say no to that?
			</p>
		</li>
		<li class="fade-in">
			<p>
				<strong>Create awareness</strong>: Collect user feedback and stories from people with
				disabilities. Real talk, these personal stories are super convincing and prove how important
				accessibility is.
			</p>
		</li>
		<li class="fade-in">
			<p>
				<strong>Team up with allies</strong>: You don’t have to be a lone wolf. Identify coworkers
				who are supportive of accessibility and willing to advocate alongside you. Working together
				with like-minded individuals can be more effective in promoting change. And it offers much
				needed support.
			</p>
		</li>
		<li class="fade-in">
			<p>
				<strong>Bringing in the pros</strong>: Sometimes, it’s wise to get accessibility experts or
				consultants on board. Their expertise can carry more weight in persuading others, and this
				might just do the trick.
			</p>
		</li>
		<li class="fade-in">
			<p>
				<strong>Legal talk</strong>: If all else fails, bring up the legal side of accessibility.
				This is especially important if your work needs to comply with strict accessibility laws.
				Non-compliance can lead to legal risks.
			</p>
		</li>
		<li class="fade-in">
			<p>
				<strong>Baby steps</strong>: Kick things off with small, manageable projects. Small
				successes can serve as examples and motivate others to embrace accessibility.
			</p>
		</li>
	</ul>

	<div class="fade-in">
		<h2 id="lets-be-real">
			Let’s be real<a
				href="#lets-be-real"
				aria-label="Permalink to &quot;Let’s be real&quot;"
				class="post-single__heading-target"
			></a>
		</h2>
		<p>
			Let’s face it, while it may all sound simple, there are plenty of challenges to overcome.
			Resistance to change can often emerge as a big barrier to accessibility efforts. If the
			organizational culture doesn’t embrace accessibility, it can be difficult for a single person
			to achieve meaningful change.
		</p>
	</div>

	<div class="fade-in">
		<p>
			To protect your well-being, choosing your battles wisely can be immensely beneficial.
			Sometimes, striving for a perfect 100% score may not be realistic. Embracing the value of
			compromise can lead to pragmatic solutions and positive outcomes. Moreover, every situation
			can serve as a valuable learning experience; it’s essential to continually reflect on what can
			be learned from past mistakes.
		</p>
	</div>

	<div class="fade-in">
		<p>
			But if you’re giving it your all and still hitting a brick wall, it’s crucial to take care of
			yourself. Reach out to a colleague or supervisor within your company to discuss these
			challenges.
		</p>
	</div>

	<div class="fade-in">
		<h2 id="conclusion">
			Conclusion<a
				href="#conclusion"
				aria-label="Permalink to &quot;Conclusion&quot;"
				class="post-single__heading-target"
			></a>
		</h2>
		<p>
			Accessibility is not just a developer’s responsibility; it’s a collective effort that involves
			the entire team. It’s about working together, from designers and developers to project
			managers and content creators, to weave accessibility into the fabric of our projects.
		</p>
	</div>

	<div class="fade-in">
		<p>
			Shifting left, as we’ve seen at GRRR, is about integrating accessibility from the very
			beginning of a project, and it’s proven to be a step in the right direction. It’s about
			identifying potential pitfalls early, ensuring that accessibility is not an afterthought but a
			core principle. Through a shared commitment to accessibility, we can build a digital world
			that welcomes and serves all users, one step at a time.
		</p>
	</div>
